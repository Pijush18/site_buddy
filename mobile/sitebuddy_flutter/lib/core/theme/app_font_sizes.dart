/// lib/core/theme/app_font_sizes.dart
///
/// PURPOSE:
/// Strict typography size scale for SiteBuddy text elements.
///
/// SCALE:
/// - title: 20.0
/// - subtitle: 16.0
/// - tab: 14.0
///
/// REQUIREMENTS:
/// - Static constants only
/// - No dependencies
/// - Production ready
class AppFontSizes {
  AppFontSizes._();

  /// Font size for section headers and primary screen titles.
  static const double title = 20.0;

  /// Font size for descriptive labels and secondary text.
  static const double subtitle = 16.0;

  /// Font size for navigation and tab-level labels.
  static const double tab = 14.0;
}



