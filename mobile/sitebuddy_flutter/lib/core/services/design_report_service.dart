          canvas.setStrokeColor(PdfColors.black);
          canvas.setLineWidth(1.5);
          canvas.drawRect(cx - w / 2, cy - h / 2, w, h);
          canvas.strokePath();

          // Main Rebar (Horizontal dashes)
          canvas.setStrokeColor(PdfColors.black);
          canvas.setLineWidth(1);
          for (int i = 0; i < 5; i++) {
            final y = cy - h / 2 + 15 + i * 18;
            canvas.drawLine(cx - w / 2 + 5, y, cx + w / 2 - 5, y);
          }
          canvas.strokePath();

          // Distribution (Vertical dots/lines)
          canvas.setStrokeColor(PdfColors.grey600);
          for (int i = 0; i < 8; i++) {
            final x = cx - w / 2 + 20 + i * 20;
            canvas.drawLine(x, cy - h / 2 + 5, x, cy + h / 2 - 5);
          }
          canvas.strokePath();
        },
      ),
    );
  }

  static pw.Widget _buildFootingDiagram(FootingDesignState state) {
    return pw.Container(
      width: 200,
      height: 200,
      decoration: pw.BoxDecoration(
        border: pw.Border.all(color: PdfColors.grey300, width: 0.5),
      ),
      child: pw.CustomPaint(
        size: const PdfPoint(200, 200),
        painter: (canvas, size) {
          final cx = size.x / 2;
          final cy = size.y / 2;
          final fSize = 150.0;

          // Footing Outline
          canvas.setStrokeColor(PdfColors.black);
          canvas.setLineWidth(1.5);
          canvas.drawRect(cx - fSize / 2, cy - fSize / 2, fSize, fSize);
          canvas.strokePath();

          // Column Outline
          final cSize = 40.0;
          canvas.drawRect(cx - cSize / 2, cy - cSize / 2, cSize, cSize);
          canvas.strokePath();

          // Rebar Grid
          canvas.setStrokeColor(PdfColors.grey500);
          canvas.setLineWidth(0.5);
          for (int i = 0; i < 6; i++) {
            final offset = -fSize / 2 + 20 + i * 22;
            canvas.drawLine(
              cx - fSize / 2 + 10,
              cy + offset,
              cx + fSize / 2 - 10,
              cy + offset,
            );
            canvas.drawLine(
              cx + offset,
              cy - fSize / 2 + 10,
              cx + offset,
              cy + fSize / 2 - 10,
            );
          }
          canvas.strokePath();
        },
      ),
    );
  }
