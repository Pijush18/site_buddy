  static pw.Widget _buildBeamDiagram(BeamDesignState state) {
    return pw.Container(
      width: 250,
      height: 180,
      decoration: pw.BoxDecoration(
        border: pw.Border.all(color: PdfColors.grey300, width: 0.5),
      ),
      child: pw.CustomPaint(
        size: const PdfPoint(250, 180),
        painter: (canvas, size) {
          const padding = 40.0;
          final drawWidth = size.x - 2 * padding;
          final drawHeight = size.y - 2 * padding;

          final cx = size.x / 2;
          final cy = size.y / 2;

          // Draw Section
          canvas.setStrokeColor(PdfColors.black);
          canvas.setLineWidth(1.5);
          canvas.drawRect(
            cx - drawWidth / 2,
            cy - drawHeight / 2,
            drawWidth,
            drawHeight,
          );
          canvas.strokePath();

          // Stirrup
          canvas.setStrokeColor(PdfColors.grey700);
          canvas.setLineWidth(1);
          const sPadding = 5.0;
          canvas.drawRect(
            cx - drawWidth / 2 + sPadding,
            cy - drawHeight / 2 + sPadding,
            drawWidth - 2 * sPadding,
            drawHeight - 2 * sPadding,
          );
          canvas.strokePath();

          // Main Bars (Bottom)
          canvas.setFillColor(PdfColors.black);
          const barRadius = 2.5;
          final rebarY = cy + drawHeight / 2 - 10;
          final startX = cx - drawWidth / 2 + 10;
          final endX = cx + drawWidth / 2 - 10;

          for (int i = 0; i < state.numBars; i++) {
            final x = state.numBars > 1
                ? startX + (endX - startX) * (i / (state.numBars - 1))
                : cx;
            canvas.drawEllipse(x, rebarY, barRadius, barRadius);
            canvas.fillPath();
          }

          // Hanger Bars (Top)
          canvas.drawEllipse(
            startX,
            cy - drawHeight / 2 + 10,
            barRadius,
            barRadius,
          );
          canvas.drawEllipse(
            endX,
            cy - drawHeight / 2 + 10,
            barRadius,
            barRadius,
          );
          canvas.fillPath();
        },
      ),
    );
  }

