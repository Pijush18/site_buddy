import 'package:flutter/material.dart';
import 'package:intl/intl.dart';

class AiShareReportCard extends StatelessWidget {
  final String question;
  final String answer;
  final String projectName;

  const AiShareReportCard({
    super.key,
    required this.question,
    required this.answer,
    required this.projectName,
  });

  String _formattedDate() {
    return DateFormat('dd MMM yyyy').format(DateTime.now());
  }

  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.symmetric(horizontal: AppLayout.pSmall),
      child: SbCard(
        padding: const EdgeInsets.all(AppLayout.pMedium),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.stretch,
          mainAxisSize:
              MainAxisSize.min, // Keep card wrapped safely around text
          children: [
            // HEADER
            Container(
              padding: const EdgeInsets.only(bottom: AppLayout.pSmall),

              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(
                    'SITE BUDDY REPORT',
                    style: AppTextStyles.sectionTitle(context).copyWith(
                      letterSpacing: 1.2,
                      color: Theme.of(context).colorScheme.onSurface,
                    ),
                  ),
                  const SizedBox(height: AppLayout.pTiny),
                  Text(
                    'Project: $projectName',
                    style: AppTextStyles.caption(context).copyWith(
                      color: Theme.of(context).colorScheme.onSurface,
                    ),
                  ),
                  Text(
                    'Date: ${_formattedDate()}',
                    style: AppTextStyles.body(context, secondary: true).copyWith(
                      color: Theme.of(context).colorScheme.onSurfaceVariant,
                    ),
                  ),
                ],
              ),
            ),
            AppLayout.vGap16,

            // BODY: Question
            Text(
              'Question:',
              style: AppTextStyles.sectionTitle(context).copyWith(
                color: Theme.of(context).colorScheme.onSurface,
              ),
            ),
            AppLayout.vGap8,
            Text(question, style: AppTextStyles.body(context)),

            AppLayout.vGap16,

            // BODY: Answer
            Text(
              'Answer:',
              style: AppTextStyles.sectionTitle(context).copyWith(
                color: Theme.of(context).colorScheme.onSurface,
              ),
            ),
            AppLayout.vGap8,
            Text(answer, style: AppTextStyles.body(context)),

            AppLayout.vGap16,

            // FOOTER BRANDING
            Container(
              padding: const EdgeInsets.only(top: AppLayout.pSmall),

              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(
                    'Generated by Site Buddy',
                    style: AppTextStyles.body(context, secondary: true).copyWith(
                      color: Theme.of(context).colorScheme.onSurfaceVariant,
                    ),
                  ),
                  Text(
                    'Developed by Er. Pijush Debbarma',
                    style: AppTextStyles.body(context, secondary: true).copyWith(
                      color: Theme.of(context).colorScheme.onSurfaceVariant.withValues(alpha: 0.8),
                    ),
                  ),
                ],
              ),
            ),
          ],
        ),
      ),
    );
  }
}