import 'package:flutter/material.dart';
import 'package:intl/intl.dart';

class AiShareReportCard extends StatelessWidget {
  final String question;
  final String answer;
  final String projectName;

  const AiShareReportCard({
    super.key,
    required this.question,
    required this.answer,
    required this.projectName,
  });

  String _formattedDate() {
    return DateFormat('dd MMM yyyy').format(DateTime.now());
  }

  @override
  Widget build(BuildContext context) {
    final theme = Theme.of(context);
    
    return Container(
      padding: SbSpacing.horizontalSM,
      child: SbCard(
        padding: SbSpacing.paddingLG,
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.stretch,
          mainAxisSize: MainAxisSize.min, // Keep card wrapped safely around text
          children: [
            // HEADER
            Container(
              padding: SbSpacing.verticalSM.copyWith(top: 0),
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(
                    'SITE BUDDY REPORT',
                    style: theme.textTheme.titleMedium!,
                  ),
                  const SizedBox(height: SbSpacing.xs),
                  Text(
                    'Project: $projectName',
                    style: theme.textTheme.labelMedium!,
                  ),
                  Text(
                    'Date: ${_formattedDate()}',
                    style: theme.textTheme.bodyMedium!,
                  ),
                ],
              ),
            ),
            const SizedBox(height: SbSpacing.lg),

            // BODY: Question
            Text(
              'Question:',
              style: theme.textTheme.titleMedium!,
            ),
            const SizedBox(height: SbSpacing.sm),
            Text(question, style: theme.textTheme.bodyLarge!),

            const SizedBox(height: SbSpacing.lg),

            // BODY: Answer
            Text(
              'Answer:',
              style: theme.textTheme.titleMedium!,
            ),
            const SizedBox(height: SbSpacing.sm),
            Text(answer, style: theme.textTheme.bodyLarge!),

            const SizedBox(height: SbSpacing.lg),

            // FOOTER BRANDING
            Container(
              padding: SbSpacing.verticalSM.copyWith(bottom: 0),
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(
                    'Generated by Site Buddy',
                    style: theme.textTheme.bodyMedium!,
                  ),
                  Text(
                    'Developed by Er. Pijush Debbarma',
                    style: theme.textTheme.bodyMedium!,
                  ),
                ],
              ),
            ),
          ],
        ),
      ),
    );
  }
}








